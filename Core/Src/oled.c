void OLED_Init(void)
{
    /* 1. 强制开启 GPIOB 组的外设时钟 */
    __HAL_RCC_GPIOB_CLK_ENABLE();

    /* 2. 强行初始化 PB6(SCL) 和 PB7(SDA) 为推挽输出、内部上拉模式 */
    GPIO_InitTypeDef GPIO_InitStructForOLED = {0};
    GPIO_InitStructForOLED.Pin = OLED_SCL_PIN | OLED_SDA_PIN;
    GPIO_InitStructForOLED.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStructForOLED.Pull = GPIO_PULLUP;
    GPIO_InitStructForOLED.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(GPIOB, &GPIO_InitStructForOLED);

    /* 3. 接下来承接原有的 OLED 硬件复位与初始化序列 */
    HAL_Delay(100);
    
    OLED_WriteCmd(0xAE);  /* 关闭显示 */
    OLED_WriteCmd(0xD5);  /* 设置时钟分频因子 */
    OLED_WriteCmd(0x80);
    OLED_WriteCmd(0xA8);  /* 设置驱动路数 */
    OLED_WriteCmd(0x3F);
    OLED_WriteCmd(0xD3);  /* 设置显示偏移 */
    OLED_WriteCmd(0x00);
    OLED_WriteCmd(0x40);  /* 设置显示开始行 */
    OLED_WriteCmd(0x8D);  /* 电荷泵设置 */
    OLED_WriteCmd(0x14);  /* 开启电荷泵升压点亮屏幕 */
    OLED_WriteCmd(0x20);  /* 设置内存地址模式 */
    OLED_WriteCmd(0x02);  /* 页寻址模式 */
    OLED_WriteCmd(0xA1);  /* 段重定义设置 */
    OLED_WriteCmd(0xC8);  /* COM扫描方向 */
    OLED_WriteCmd(0xDA);  /* COM硬件引脚配置 */
    OLED_WriteCmd(0x12);
    OLED_WriteCmd(0x81);  /* 对比度设置 */
    OLED_WriteCmd(0xCF);
    OLED_WriteCmd(0xD9);  /* 设置预充电周期 */
    OLED_WriteCmd(0xF1);
    OLED_WriteCmd(0xDB);  /* VCOMH取消选择级别 */
    OLED_WriteCmd(0x30);
    OLED_WriteCmd(0xA4);  /* 全局显示开启 */
    OLED_WriteCmd(0xA6);  /* 设置正常显示 */
    OLED_WriteCmd(0xAF);  /* 开启显示 */
    
    OLED_Clear();
}