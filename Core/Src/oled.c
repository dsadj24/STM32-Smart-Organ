
#include "oled.h"
#include "gpio.h"

/* 软件模拟 I2C 延时函数 */
static void OLED_I2C_Delay(void)
{
    uint8_t i = 10;
    while(i--);
}

/* 设置 SCL 引脚电平 */
static void OLED_SCL_Set(uint8_t level)
{
    HAL_GPIO_WritePin(OLED_SCL_PORT, OLED_SCL_PIN, level ? GPIO_PIN_SET : GPIO_PIN_RESET);
}

/* 设置 SDA 引脚电平 */
static void OLED_SDA_Set(uint8_t level)
{
    HAL_GPIO_WritePin(OLED_SDA_PORT, OLED_SDA_PIN, level ? GPIO_PIN_SET : GPIO_PIN_RESET);
}

/* I2C 起始信号 */
void OLED_I2C_Start(void)
{
    OLED_SDA_Set(1);
    OLED_SCL_Set(1);
    OLED_I2C_Delay();
    OLED_SDA_Set(0);
    OLED_I2C_Delay();
    OLED_SCL_Set(0);
}

/* I2C 停止信号 */
void OLED_I2C_Stop(void)
{
    OLED_SDA_Set(0);
    OLED_SCL_Set(1);
    OLED_I2C_Delay();
    OLED_SDA_Set(1);
    OLED_I2C_Delay();
}

/* I2C 写入字节 */
void OLED_I2C_WriteByte(uint8_t byte)
{
    uint8_t i;
    for(i = 0; i < 8; i++)
    {
        OLED_SDA_Set((byte & 0x80) >> 7);
        byte <<= 1;
        OLED_SCL_Set(1);
        OLED_I2C_Delay();
        OLED_SCL_Set(0);
        OLED_I2C_Delay();
    }
    /* 等待 ACK */
    OLED_SDA_Set(1);
    OLED_SCL_Set(1);
    OLED_I2C_Delay();
    OLED_SCL_Set(0);
    OLED_I2C_Delay();
}

/* 写入命令 */
void OLED_WriteCmd(uint8_t cmd)
{
    OLED_I2C_Start();
    OLED_I2C_WriteByte(0x78);  /* OLED 地址 */
    OLED_I2C_WriteByte(OLED_CMD);
    OLED_I2C_WriteByte(cmd);
    OLED_I2C_Stop();
}

/* 写入数据 */
void OLED_WriteData(uint8_t data)
{
    OLED_I2C_Start();
    OLED_I2C_WriteByte(0x78);  /* OLED 地址 */
    OLED_I2C_WriteByte(OLED_DATA);
    OLED_I2C_WriteByte(data);
    OLED_I2C_Stop();
}

